        // Main application logic
        document.addEventListener('DOMContentLoaded', function() {
            // Cache DOM elements
            const elements = {
                downloadBtn: document.getElementById('downloadBtn'),
                loader: document.getElementById('loader'),
                resultContainer: document.getElementById('resultContainer'),
                reelUrl: document.getElementById('reelUrl'),
                finalDownloadBtn: document.getElementById('finalDownloadBtn'),
                previewImage: document.getElementById('previewImage'),
                resultTitle: document.getElementById('resultTitle'),
                resultMessage: document.getElementById('resultMessage'),
                downloadCount: document.getElementById('downloadCount'),
                clearBtn: document.getElementById('clearBtn')
            };

            // CSRF Token management
            let csrfToken = null;

            async function getCsrfToken() {
                if (!csrfToken) {
                    try {
                        const response = await fetch('/csrf-token');
                        const data = await response.json();
                        csrfToken = data.token;
                    } catch (error) {
                        console.warn('CSRF token fetch failed:', error);
                    }
                }
                return csrfToken;
            }

            // Animate counter
            function animateCounter(element, start, end, duration) {
                if (!element) return;
                
                let startTime = null;
                const step = (timestamp) => {
                    if (!startTime) startTime = timestamp;
                    const progress = Math.min((timestamp - startTime) / duration, 1);
                    const value = Math.floor(progress * (end - start) + start);
                    element.textContent = value.toLocaleString();
                    if (progress < 1) {
                        requestAnimationFrame(step);
                    }
                };
                requestAnimationFrame(step);
            }

            // Load and animate stats
            async function loadStats() {
                try {
                    const response = await fetch('/instagram/stats');
                    const data = await response.json();
                    animateCounter(elements.downloadCount, 0, data.downloads, 2000);
                } catch (error) {
                    animateCounter(elements.downloadCount, 0, 5231897, 2000);
                }
            }

            // API call to backend
            async function downloadInstagramMedia(url) {
                const token = await getCsrfToken();
                
                const response = await fetch('/instagram/get-media', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': token || '',
                        'Accept': 'application/json'
                    },
                    body: JSON.stringify({ url })
                });

                if (!response.ok) {
                    const errorData = await response.json();
                    throw new Error(errorData.message || `Server Error: ${response.status}`);
                }

                return await response.json();
            }

            // Show notification
            function showNotification(message, type = 'success') {
                const notification = document.createElement('div');
                notification.className = `notification ${type}`;
                notification.style.cssText = `
                    position: fixed;
                    top: 20px;
                    right: 20px;
                    background: ${type === 'success' ? 'var(--primary)' : '#e74c3c'};
                    color: white;
                    padding: 1rem 1.5rem;
                    border-radius: 10px;
                    box-shadow: 0 5px 15px rgba(0,0,0,0.2);
                    z-index: 10000;
                    max-width: 300px;
                    animation: slideIn 0.3s ease;
                `;
                
                notification.innerHTML = `
                    <div style="display: flex; align-items: center; gap: 10px;">
                        <span>${type === 'success' ? '‚úÖ' : '‚ùå'}</span>
                        <span>${message}</span>
                        <button onclick="this.parentElement.parentElement.remove()" style="background: none; border: none; color: white; font-size: 18px; cursor: pointer; margin-left: auto;">√ó</button>
                    </div>
                `;
                
                document.body.appendChild(notification);
                setTimeout(() => notification.remove(), 5000);
            }

            // Main download handler
            elements.downloadBtn?.addEventListener('click', async function() {
                const url = elements.reelUrl.value.trim();

                // Validation
                if (!url) {
                    showNotification('Please enter an Instagram Reel URL', 'error');
                    elements.reelUrl.focus();
                    return;
                }

                if (!url.includes('instagram.com/')) {
                    showNotification('Please enter a valid Instagram URL', 'error');
                    elements.reelUrl.focus();
                    return;
                }

                // Show loading state
                elements.loader.style.display = 'block';
                elements.resultContainer.style.display = 'none';
                this.disabled = true;
                this.innerHTML = '<span>‚è≥</span> Processing...';

                try {
                    const response = await downloadInstagramMedia(url);

                    if (response?.success && response?.data) {
                        const media = response.data;
                        
                        // Show result
                        elements.resultContainer.style.display = 'block';
                        elements.resultTitle.textContent = `${media.type === 'video' ? 'Video' : 'Image'} Ready!`;
                        elements.resultMessage.textContent = `Quality: ${media.quality || 'HD'} - Click to download`;

                        // Set preview
                        if (media.thumbnail_proxy && elements.previewImage) {
                            elements.previewImage.src = media.thumbnail_proxy;
                            elements.previewImage.style.display = 'block';
                            elements.previewImage.onerror = () => elements.previewImage.style.display = 'none';
                        }

                        // Setup download button
                        if (media.download_url && elements.finalDownloadBtn) {
                            elements.finalDownloadBtn.style.display = 'inline-flex';
                            elements.finalDownloadBtn.onclick = function(e) {
                                e.preventDefault();
                                
                                // Update counter
                                const currentCount = parseInt(elements.downloadCount.textContent.replace(/,/g, ''));
                                animateCounter(elements.downloadCount, currentCount, currentCount + 1, 500);
                                
                                // Start download
                                const link = document.createElement('a');
                                link.href = media.download_url;
                                link.download = `instagram_reel_${Date.now()}`;
                                link.style.display = 'none';
                                document.body.appendChild(link);
                                link.click();
                                document.body.removeChild(link);
                                
                                showNotification('Download started!');
                                
                                // Reset after delay
                                setTimeout(() => location.reload(), 2000);
                            };
                        }

                        showNotification('Media ready for download!');
                    } else {
                        throw new Error('No media found');
                    }
                } catch (error) {
                    console.error('Download error:', error);
                    showNotification('Download failed. Please try again.', 'error');
                } finally {
                    elements.loader.style.display = 'none';
                    this.disabled = false;
                    this.innerHTML = '<span>‚¨á</span> Download Now';
                }
            });

            // Clear button handler
            elements.clearBtn?.addEventListener('click', function() {
                elements.reelUrl.value = '';
                elements.resultContainer.style.display = 'none';
                if (elements.previewImage) {
                    elements.previewImage.src = '';
                    elements.previewImage.style.display = 'none';
                }
                if (elements.finalDownloadBtn) {
                    elements.finalDownloadBtn.style.display = 'none';
                }
                elements.reelUrl.focus();
            });

            // Smooth scrolling for anchor links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function(e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({ behavior: 'smooth' });
                    }
                });
            });

